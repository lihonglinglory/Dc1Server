package bean;

/**
 * 状态查询
 */
public class StatusBean {
    /**
     * status : 1011
     * I : 119
     * V : 219
     * P : 10
     */

    private String status;
    private int I;
    private int V;
    private int P;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getI() {
        return I;
    }

    public void setI(int I) {
        this.I = I;
    }

    public int getV() {
        return V;
    }

    public void setV(int V) {
        this.V = V;
    }

    public int getP() {
        return P;
    }

    public void setP(int P) {
        this.P = P;
    }
}
